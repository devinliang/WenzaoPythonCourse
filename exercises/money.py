貨幣的幣值有千元鈔,百元鈔,伍拾元硬幣,拾元硬幣與壹元硬幣,
根據金額可以換算出需要支付的貨幣個數

請設計一個程式,輸入一個金額,然後顯示需要支付的貨幣個數
